public class Primitives {
    public static void main(String[] args) {
        int integer = 5;
        boolean isOn = false;
        char character = 'a';
        float floaty = 1.234f;

        System.out.println("integer = " + integer);
        System.out.println("isOn = " + isOn);
        System.out.println("character = " + character);
        System.out.println("floaty = " + floaty);
    }
    
}
